// SWITCH CASE OPERATOR IN JAVASCRIPT

let day = 1;

switch (day) {
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wednsday");
        break;
    case 4:
        console.log("Thursday");
        break;
    case 5:
        console.log("Friday");
        break;
    case 6: 
        console.log("Saturday");
        break;
    case 7:
        console.log("Sunday");
        break;               
    default:
        console.log("Invalid day");
        break;
}

// case is used to check the value of the variable
// break is used to stop the switch case from executing further
// default is used to execute if none of the cases are true




