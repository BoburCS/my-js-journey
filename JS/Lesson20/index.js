// Topic: Async/Await
// These are keywords that allow you to write asynchronous code in a synchronous manner.
// async: This keyword is used to declare an asynchronous function. This function returns a promise. If the function throws an error, the promise will be rejected. If the function returns a value, the promise will be resolved with that value.
// await: This keyword is used inside an async function to wait for a promise. It pauses the execution of the function and waits for the promise's resolution, and then resumes the function's execution and returns the resolved value.


